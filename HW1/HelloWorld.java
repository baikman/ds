// This is the class declaration. Every Java program must have at least one class.
public class HelloWorld {

    // The main method is the entry point of any Java application.
    // The 'String[] args' parameter allows command-line arguments to be passed.
    public static void main(String[] args) {

        // This line prints the text "Hello, World!" to the console.
        // 'System.out' is the standard output stream, and 'println' prints with a newline.
        System.out.println("Hello, World!");
    }
}